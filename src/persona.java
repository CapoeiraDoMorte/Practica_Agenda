public class persona {
    private String nombre;
    private int edad;
    private String dni;

    public persona() {
        this.nombre = nombre;
        this.edad = edad;
        this.dni = dni;
    }

    public persona(String nombre, int edad) {
        this.nombre = nombre;
        this.edad = edad;
    }

    public persona(String nombre, int edad, String dni) {
        this.nombre = nombre;
        this.edad = edad;
        this.dni = dni;
    }
}
